package com.byname;

public class Student
{
	private int rollno;
	private String name;
	private Address ex;
	
	public int getRollno() {
		return rollno;
	}
	public void setRollno(int rollno) {
		this.rollno = rollno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Address getEx() {
		return ex;
	}
	public void setEx(Address ex) {
		this.ex = ex;
	}
	
	
}
